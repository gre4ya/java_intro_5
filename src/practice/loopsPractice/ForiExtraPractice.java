package practice.loopsPractice;

public class ForiExtraPractice {
    public static void main(String[] args) {

        System.out.println("\n---Hardcoded---\n");

        System.out.println("Number: " + "1");
        System.out.println("Number: " + "-1\n");

        System.out.println("Number: " + "2");
        System.out.println("Number: " + "-2\n");

        System.out.println("Number: " + "3");
        System.out.println("Number: " + "-3\n");

        System.out.println("Number: " + "4");
        System.out.println("Number: " + "-4\n");

        System.out.println("Number: " + "5");
        System.out.println("Number: " + "-5");

        System.out.println("\n---Fori number counter ---\n");

        for (int i = 1, j = -1; i <= 5 && j > -6; i++, j--) {
            System.out.println("Number: " + i);
            System.out.println("Number: " + j + "\n");




        }
    }
}
